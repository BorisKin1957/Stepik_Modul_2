'''Вам нужно подсчитать количество домов, цена которых выше средней и вывести это значение.'''

prices = [125000, 78000, 110000, 65000, 300000, 250000, 210000, 150000, 165000,
          140000, 125000, 85000, 90000, 128000, 230000, 225000, 100000, 300000]

mean_price = sum(prices) / len(prices)

print(len([price for price in prices if price > mean_price]))